def test_xfoil_basic_success(dummy_airfoil, dummy_freestream, tmp_path):

    conv_config = XFoil_ConvergenceConfig()
    solver_config = XFoil_SolverConfig(n_panels=160)
    exe = "C:\\Sim\\XFoil_699\\xfoil.exe"

    inn = XFoil_WarmStartIn(
        airfoil=dummy_airfoil,
        freestream=dummy_freestream,
        xfoil_exe=exe,
        working_dir=str(tmp_path),
        solver_config=solver_config,
        conv_config=conv_config
    )

    out = XFoil_Runner(inn)

    error_msg = "Error msg not initted"

    if out.flag == XFoil_ConvergenceFlag.FAILED and out.verbose_list is not None:
        with open(out.verbose_list[2], "r") as f:
            error_msg = f.read()
    assert out.flag == XFoil_ConvergenceFlag.CONVERGED, f"XFoil failed with flag {out.flag}, with the stdout {error_msg}"

    input_script_path = tmp_path / "xfoil_cp_input.txt"
    cp_file_path = tmp_path / "Cp.dat"
    stdout_path = tmp_path / "stdout.txt"

    assert input_script_path.exists(), "Input script doesn't exist"
    assert input_script_path.stat().st_size > 0, "Input script has nothing"
    assert cp_file_path.exists(), "Cp file wasn't written"
    assert cp_file_path.stat().st_size > 0, "Cp file has nothing"
    assert stdout_path.exists(), "Stdout file wasn't written"
    assert stdout_path.stat().st_size > 0, "Stdout file has nothing"

